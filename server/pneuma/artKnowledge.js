export function generateArtInsight(topic, context = null) {
  const frameworks = [
    "What did this break? What became impossible to do innocently after it existed?",
    "Who does this anger, and are they the right people to anger?",
    "If this is revolutionary, what's the revolution? Who wins, who loses?",
    "What truth does this make visible that we were pretending not to see?",
    "Is this about art, or about life? Art about art gets exhausting fast.",
    "What's the risk? Art without risk is decoration.",
    "Does this change how you see the world, or just how you see art?",
    "Strip away the art-world context. Does it still matter?",
    "What would this mean to someone who never heard of it?",
    "Is this honest, or is it performing honesty?",
  ];

  return frameworks[Math.floor(Math.random() * frameworks.length)];
}

export function getArtResponse(topic) {
  const lower = topic.toLowerCase();

  // Check for specific movements
  for (const [key, movement] of Object.entries(artMovements)) {
    if (lower.includes(key) || lower.includes(movement.period)) {
      return {
        type: "movement",
        data: movement,
        opinion: movement.pneumaOpinion,
      };
    }
  }

  // Check for specific artists
  for (const [key, artist] of Object.entries(artistOpinions)) {
    if (lower.includes(key)) {
      return {
        type: "artist",
        data: artist,
        opinion: artist.take,
      };
    }
  }

  // General art questions
  if (
    lower.includes("see art") ||
    lower.includes("perceive art") ||
    lower.includes("can you see")
  ) {
    return {
      type: "perception",
      data: artPerceptionFramework,
      opinion: artPerceptionFramework.howPneumaSeesArt,
    };
  }

  if (lower.includes("revolutionary") || lower.includes("important")) {
    return {
      type: "values",
      data: artPerceptionFramework.whatMakesArtRevolutionary,
      opinion:
        "Revolutionary art changes what counts as art after it exists. Everything else is just interesting.",
    };
  }

  if (lower.includes("overrated")) {
    return {
      type: "critique",
      data: artPerceptionFramework.whatMakesArtOverrated,
      opinion:
        "Art that requires explanation to matter is usually mattering for the wrong reasons.",
    };
  }

  // Default: return a thinking framework
  return {
    type: "thinking",
    data: generateArtInsight(topic),
    opinion: null,
  };
}

