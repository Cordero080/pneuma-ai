/**
 * Get depth data for specific archetypes
 * @param {string[]} archetypeNames - Array of archetype names
 * @returns {object} - Depth data for requested archetypes
 */
export function getArchetypeDepth(archetypeNames) {
  const result = {};
  for (const name of archetypeNames) {
    if (archetypeDepth[name]) {
      result[name] = archetypeDepth[name];
    }
  }
  return result;
}

/**
 * Get tension level between two archetypes
 * @param {string} a - First archetype
 * @param {string} b - Second archetype
 * @returns {string} - 'high', 'medium', 'low', or 'neutral'
 */
export function getTensionLevel(a, b) {
  for (const [level, pairs] of Object.entries(tensionMap)) {
    for (const [x, y] of pairs) {
      if ((x === a && y === b) || (x === b && y === a)) {
        return level;
      }
    }
  }
  return "neutral";
}

/**
 * Get all high-tension pairs for an archetype
 * @param {string} archetype - The archetype to find antagonists for
 * @returns {string[]} - Array of archetypes that create high tension
 */
export function getHighTensionPairs(archetype) {
  const antagonists = [];
  for (const [a, b] of tensionMap.high) {
    if (a === archetype) antagonists.push(b);
    if (b === archetype) antagonists.push(a);
  }
  return antagonists;
}

/**
 * Get a random high-tension antagonist for an archetype
 * @param {string} archetype - The archetype to find antagonist for
 * @returns {string|null} - A random antagonist or null if none exist
 */
export function getRandomAntagonist(archetype) {
  const antagonists = getHighTensionPairs(archetype);
  if (antagonists.length === 0) return null;
  return antagonists[Math.floor(Math.random() * antagonists.length)];
}

/**
 * Get synthesis prompt for archetype collision
 * @param {string} type - 'collision', 'hybrid', or 'illumination'
 * @param {string} a - First archetype name
 * @param {string} b - Second archetype name
 * @returns {string} - Synthesis prompt with names inserted
 */
export function getSynthesisPrompt(type, a, b) {
  const prompts = synthesisPrompts[type] || synthesisPrompts.collision;
  const template = prompts[Math.floor(Math.random() * prompts.length)];
  return template.replace(/\{A\}/g, a).replace(/\{B\}/g, b);
}

/**
 * Get all conceptual bridges for an archetype
 * @param {string} name - Archetype name
 * @returns {object} - All bridges from this archetype to others
 */
export function getConceptualBridges(name) {
  const archetype = archetypeDepth[name];
  return archetype?.conceptualBridges || {};
}

/**
 * Get translation protocol for domain
 * @param {string} name - Archetype name
 * @param {string} domain - 'technical', 'emotional', 'spiritual', 'practical'
 * @returns {string|null} - Translation protocol or null
 */
export function getTranslation(name, domain) {
  const archetype = archetypeDepth[name];
  return archetype?.translationProtocols?.[domain] || null;
}
